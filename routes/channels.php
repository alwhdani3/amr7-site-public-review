<?php

use Illuminate\Support\Facades\Broadcast;
use App\Models\Ticket;

/*
|--------------------------------------------------------------------------
| 1) قناة المستخدم الخاصة (للإشعارات الشخصية)
|--------------------------------------------------------------------------
| تسمح للمستخدم بالاستماع فقط للقناة التي تحمل الـ ID الخاص به.
*/
Broadcast::channel('App.Models.User.{id}', function ($user, $id) {
    return (int) $user->id === (int) $id;
});

/*
|--------------------------------------------------------------------------
| 2) قناة المحادثة داخل التذكرة (Chat Room)
|--------------------------------------------------------------------------
| هذه القناة ضرورية لصفحة الدردشة في التذاكر.
| تسمح بالدخول إذا كان المستخدم هو صاحب التذكرة أو موظف دعم (Agent).
*/
Broadcast::channel('tickets.{ticketId}', function ($user, $ticketId) {
    // نتحقق إذا كان المستخدم موظف/أدمن (استخدام الدالة التي أضفناها في User Model)
    if ($user->isAdmin() || $user->isAgent()) {
        return true;
    }

    // أو نتحقق إذا كان المستخدم هو صاحب التذكرة
    $ticket = Ticket::find($ticketId);
    return $ticket && (int) $ticket->user_id === (int) $user->id;
});

/*
|--------------------------------------------------------------------------
| 3) قناة لوحة التحكم (Dashboard / Admins)
|--------------------------------------------------------------------------
| لإرسال إشعارات فورية للموظفين عند وصول تذكرة جديدة أو طلب جديد.
*/
Broadcast::channel('admins', function ($user) {
    return $user->isAdmin() || $user->isAgent();
});